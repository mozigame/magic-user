package com.magic.user.bean;

/**
 * Register
 *
 * @author zj
 * @date 2017/5/6
 */
public class Register {

    /**
     * 开始时间
     */
    private Long start;
    /**
     * 结束时间
     */
    private Long end;

    public Long getStart() {
        return start;
    }

    public void setStart(Long start) {
        this.start = start;
    }

    public Long getEnd() {
        return end;
    }

    public void setEnd(Long end) {
        this.end = end;
    }
}
